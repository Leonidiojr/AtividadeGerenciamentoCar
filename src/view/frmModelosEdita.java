    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    boolean blnValido = true;
    StringBuilder mensagemErro = new StringBuilder();

    // Validação dos campos
    if (this.jTextField1.getText().trim().isEmpty()) {
        mensagemErro.append("Nome não pode ser vazio.\n");
        this.jTextField1.setBackground(Color.RED);
        blnValido = false;
    } else {
        this.jTextField1.setBackground(Color.WHITE);
    }
    
    if (this.jTextField2.getText().trim().isEmpty()) {
        mensagemErro.append("ID do Modelo não pode ser vazio.\n");
        this.jTextField2.setBackground(Color.RED);
        blnValido = false;
    } else {
        try {
            Integer.parseInt(this.jTextField2.getText());
            this.jTextField2.setBackground(Color.WHITE);
        } catch (NumberFormatException e) {
            mensagemErro.append("ID do Modelo deve ser um número inteiro válido.\n");
            this.jTextField2.setBackground(Color.RED);
            blnValido = false;
        }
    }

    if (this.jComboBox1.getSelectedItem() == null) {
        mensagemErro.append("Tipo não pode ser vazio.\n");
        this.jComboBox1.setBackground(Color.RED);
        blnValido = false;
    } else {
        this.jComboBox1.setBackground(Color.WHITE);
    }

    if (this.jComboBox3.getSelectedItem() == null) {
        mensagemErro.append("ID da Marca não pode ser vazio.\n");
        this.jComboBox3.setBackground(Color.RED);
        blnValido = false;
    } else {
        try {
            Integer.parseInt((String) this.jComboBox3.getSelectedItem());
            this.jComboBox3.setBackground(Color.WHITE);
        } catch (NumberFormatException e) {
            mensagemErro.append("ID da Marca deve ser um número inteiro válido.\n");
            this.jComboBox3.setBackground(Color.RED);
            blnValido = false;
        }
    }

    // Se passar na validação, salva o registro
    if (blnValido) {            
        modeloAtiva.setNome(this.jTextField1.getText());
        String itemSelecionado = (String) this.jComboBox1.getSelectedItem();
        modeloAtiva.setTipo(itemSelecionado);
        itemSelecionado = (String) this.jComboBox3.getSelectedItem();
        modeloAtiva.setIdMarca(Integer.valueOf(itemSelecionado));
        modeloAtiva.setIdModeloPK(Integer.valueOf(this.jTextField2.getText()));
        System.out.println(modeloAtiva.toString());
        modeloAtiva.salvarModelo(modeloAtiva);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, mensagemErro.toString(), "Erro de Validação", JOptionPane.ERROR_MESSAGE);
    }                       
                
    }//GEN-LAST:event_jButton1ActionPerformed

