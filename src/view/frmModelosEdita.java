    private void carregarModeloAtivo(){
        
        try {
            // Define o texto dos JTextFields com as informações de modeloAtiva
            this.jTextField2.setText(String.valueOf(modeloAtiva.getIdModeloPK()));
            this.jTextField1.setText(modeloAtiva.getNome());

            // Converte o tipo do modelo para uma string
            String modeloTipoStr = modeloAtiva.getTipo(); // Supondo que modeloAtiva possui um método getTipo()
            // Seleciona o item correspondente no JComboBox usando o tipo do modelo
            this.jComboBox1.setSelectedItem(modeloTipoStr);

            // Selecione o item no jComboBox3 baseado no idMarca
            String marcaStr = String.valueOf(modeloAtiva.getIdMarca());
            this.jComboBox3.setSelectedItem(marcaStr);
            // Sincronize o índice selecionado no jComboBox5 com o jComboBox3
            this.jComboBox5.setSelectedIndex(this.jComboBox3.getSelectedIndex());
            this.jTextField1.requestFocusInWindow();
            
        } catch (Exception e) {
            e.printStackTrace(); // Loga a exceção para depuração
        }

        
    }

