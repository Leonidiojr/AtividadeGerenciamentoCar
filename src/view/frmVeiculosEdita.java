    private void carregarVeiculoAtivo(){
        
        try {
            this.jTextField1.setText(veiculoAtivo.getPlacaPK());
            this.jTextField3.setText(veiculoAtivo.getChassi());
            this.jFormattedTextField1.setText(String.valueOf(veiculoAtivo.getKm()));
            this.jFormattedTextField2.setText(String.valueOf(veiculoAtivo.getNPatrimonio()));
            this.jFormattedTextField3.setText(String.valueOf(veiculoAtivo.getAnoFabricacao()));
            this.jFormattedTextField4.setText(String.valueOf(veiculoAtivo.getAnoModelo()));      
            
            // Converta os IDs de marca e modelo para Strings, se necessário
            String marcaIdStr = String.valueOf(veiculoAtivo.getMarcaId());
            String modeloIdStr = String.valueOf(veiculoAtivo.getModeloId());

            // Selecione os itens nos JComboBoxes correspondentes aos IDs de marca e modelo
            this.jComboBox3.setSelectedItem(marcaIdStr);            
            this.jComboBox4.setSelectedItem(modeloIdStr); 
            this.jComboBox5.setSelectedIndex(this.jComboBox3.getSelectedIndex());        
            this.jComboBox6.setSelectedIndex(this.jComboBox4.getSelectedIndex());                                           

            try {

                // Busca e atualiza a propriedade no formulário
                Propriedade propriedade = new Propriedade(0,"");
                String placaFK = this.jTextField1.getText();
                propriedade = propriedadeController.buscarPropriedadeporPlaca(placaFK);
                if (propriedade != null) {
                    int idClienteFK = propriedade.getIdClienteFK()-1;
                    this.jComboBox7.setSelectedIndex(idClienteFK);
                    this.jComboBox8.setSelectedIndex(this.jComboBox7.getSelectedIndex());                                        
                }

            } catch (Exception e) {
                e.printStackTrace(); // Imprime a pilha de erros para ajudar na depuração
                JOptionPane.showMessageDialog(null,
                        "Erro ao processar a propriedade:\n" + e.getMessage(),
                        "Erro", JOptionPane.ERROR_MESSAGE);
            }
            
            this.jTextField3.requestFocusInWindow();
        } catch (Exception e) {

        }


        
    }

