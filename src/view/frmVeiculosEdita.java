    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        try {
            // Define o índice selecionado do jComboBox7 para o mesmo índice do jComboBox8
            this.jComboBox7.setSelectedIndex(this.jComboBox8.getSelectedIndex());

            int idClienteFK = this.jComboBox8.getSelectedIndex() + 1; // Ajuste o índice conforme necessário
            String placaFK = this.jTextField1.getText();

            // Verifica se a propriedade existe
            Propriedade propriedadeAtual = propriedadeController.buscarPropriedade(idClienteFK, placaFK);
            Propriedade propriedadeUltima = propriedadeController.buscarPropriedadeporPlaca(placaFK);

            if (propriedadeAtual == null) {
                JOptionPane.showMessageDialog(null, "Propriedade não encontrada, inserindo nova propriedade...");
                Propriedade novaPropriedade = new Propriedade(idClienteFK, placaFK);
                propriedadeController.criarPropriedade(novaPropriedade);
            } else {
                JOptionPane.showMessageDialog(null, "Propriedade já existe para o cliente e placa fornecidos.");

                // Insere uma nova propriedade se for diferente da última
                if (propriedadeUltima == null || propriedadeUltima.getIdClienteFK() != idClienteFK) {
                    JOptionPane.showMessageDialog(null, "Inserindo nova propriedade porque a última é diferente.");
                    Propriedade novaPropriedade = new Propriedade(idClienteFK, placaFK);
                    propriedadeController.criarPropriedade(novaPropriedade);
                } else {
                    JOptionPane.showMessageDialog(null, "A propriedade mais recente já está atualizada.");
                }
            }

        } catch (Exception e) {
            e.printStackTrace(); // Imprime a pilha de erros para ajudar na depuração
            JOptionPane.showMessageDialog(null,
                    "Erro ao processar a propriedade:\n" + e.getMessage(),
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }


        
    }//GEN-LAST:event_jButton4ActionPerformed

