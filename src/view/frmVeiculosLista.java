    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        // Solicita o código do veiculo a ser removido
        String idVeiculo = DialogBoxes.InputBox("Entre com a placa do veiculo a ser removido:", "Remover veiculo");

        // Busca o veiculo pelo código fornecido
        Veiculo veiculo = veiculoController.buscarVeiculoPorPlaca(idVeiculo);

        if (veiculo != null && veiculo.getPlacaPK()!= null) {
            int resposta = JOptionPane.showConfirmDialog(null,
                    "Veiculo a ser removido: " + veiculo.getPlacaPK() + "\nVocê tem certeza que deseja remover este veiculo?",
                    "Confirmação de Remoção",
                    JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_OPTION) {
                veiculoController.deletarVeiculo(idVeiculo);
                JOptionPane.showMessageDialog(null, "Veiculo removido com sucesso.");
                veiculoController.listarVeiculos();
                carregarVeiculos();
            } else {
                JOptionPane.showMessageDialog(null, "A remoção do veiculo não foi confirmada.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Veiculo não encontrado ou nome completo não disponível.");
        }            

    }//GEN-LAST:event_jButton3ActionPerformed

