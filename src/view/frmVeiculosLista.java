    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

       
    // Solicita o código do veículo a ser adicionado
    String idVeiculo = DialogBoxes.InputBox("Entre com a placa do veículo a ser adicionado:", "Adicionar veículo");

    // Busca o veículo pelo código fornecido
    Veiculo veiculo = veiculoController.buscarVeiculoPorPlaca(idVeiculo);

    if (veiculo != null && veiculo.getPlacaPK() != null) {
        int resposta = JOptionPane.showConfirmDialog(null,
                "Veículo já existe no sistema: " + veiculo.getPlacaPK() + "\nVocê deseja abrir as informações deste veículo?",
                "Confirmação de Remoção",
                JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {
            application.MainApp.veiculoAtivo = veiculo;
            frmVeiculosEdita cadastroVeiculos = new frmVeiculosEdita(this, true);
            cadastroVeiculos.setLocationRelativeTo(this);
            cadastroVeiculos.setVisible(true);
            veiculoController.listarVeiculos();
        } else {
            JOptionPane.showMessageDialog(null, "A visualização do veículo não foi confirmada.");
        }
    } else {
        int resposta = JOptionPane.showConfirmDialog(null,
                "Veículo não existe no sistema: " + idVeiculo + "\nVocê deseja adicionar este veículo?",
                "Confirmação de Adição",
                JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {
            Veiculo veiculoAtivo = new Veiculo(idVeiculo, 0, 0, 0, 0, "", 0.0, null);
            application.MainApp.veiculoAtivo = veiculoAtivo;
            frmVeiculosEdita cadastroVeiculos = new frmVeiculosEdita(this, true);
            cadastroVeiculos.setLocationRelativeTo(this);
            cadastroVeiculos.setVisible(true);
            
            if (!veiculoAtivo.getPlacaPK().isEmpty()) {
                veiculoController.criarVeiculo(veiculoAtivo);
            }
            carregarVeiculos();
        } else {
            JOptionPane.showMessageDialog(null, "A adição do veículo não foi confirmada.");
        }
    }

        
    }//GEN-LAST:event_jButton1ActionPerformed

