    private void carregarPecaAtivo() {
        
        BigDecimal valor1, valor2, valorTotal;
        NumberFormat currencyFormatter = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));

        try {
            // Define o texto dos JTextFields com as informações de pecaAtivo
            this.jTextField1.setText(String.valueOf(pecaAtivo.getIdPecasPK()));
            this.jTextField2.setText(pecaAtivo.getDescricao());            
            this.jTextField3.setText(String.valueOf(pecaAtivo.getQntdEstoque()));
            this.jTextField4.setText(String.valueOf(pecaAtivo.getPrecoUnit()));

            // Conversão dos valores para BigDecimal
            valor1 = new BigDecimal(pecaAtivo.getPrecoUnit().toString());
            valor2 = new BigDecimal(String.valueOf(pecaAtivo.getQntdEstoque()));

            // Multiplicação de BigDecimal
            valorTotal = valor1.multiply(valor2);

            // Define o valor total no JTextField
            this.jTextField5.setText(currencyFormatter.format(valorTotal));
            this.jTextField6.setText(String.valueOf(pecaAtivo.getIdItensFK()));
            this.jTextField7.setText(String.valueOf(pecaAtivo.getCodFabricante()));
            this.jTextField2.requestFocusInWindow();
            
        } catch (Exception e) {
            e.printStackTrace(); // Loga a exceção para depuração
        }
    }

    private void inicializarDocumentListeners() {
        // Adiciona DocumentListener aos campos de texto relevantes
        this.jTextField3.getDocument().addDocumentListener(new DocumentListener() {
            public void changedUpdate(DocumentEvent e) {
                atualizarValorTotal();
            }

            public void removeUpdate(DocumentEvent e) {
                atualizarValorTotal();
            }

            public void insertUpdate(DocumentEvent e) {
                atualizarValorTotal();
            }
        });

        this.jTextField4.getDocument().addDocumentListener(new DocumentListener() {
            public void changedUpdate(DocumentEvent e) {
                atualizarValorTotal();
            }

            public void removeUpdate(DocumentEvent e) {
                atualizarValorTotal();
            }

            public void insertUpdate(DocumentEvent e) {
                atualizarValorTotal();
            }
        });
    }

    private void atualizarValorTotal() {
        BigDecimal valor1, valor2, valorTotal;
        NumberFormat currencyFormatter = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));

        try {
            valor1 = new BigDecimal(this.jTextField4.getText().replace("R$", "").replace(",", "").trim());
        } catch (NumberFormatException e) {
            valor1 = BigDecimal.ZERO;
        }

        try {
            valor2 = new BigDecimal(this.jTextField3.getText().trim());
        } catch (NumberFormatException e) {
            valor2 = BigDecimal.ZERO;
        }

        // Multiplicação de BigDecimal
        valorTotal = valor1.multiply(valor2);

        // Define o valor total no JTextField
        this.jTextField5.setText(currencyFormatter.format(valorTotal));
    }

    
