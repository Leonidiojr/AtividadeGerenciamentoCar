    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    boolean blnValido = true;
    StringBuilder mensagemErro = new StringBuilder();

    // Validação dos campos
    if (this.jTextField1.getText().trim().isEmpty()) {
        mensagemErro.append("ID da Peça não pode ser vazio.\n");
        this.jTextField1.setBackground(Color.RED);
        blnValido = false;
    } else {
        this.jTextField1.setBackground(Color.WHITE);
    }
    
    if (this.jTextField2.getText().trim().isEmpty()) {
        mensagemErro.append("Descrição não pode ser vazia.\n");
        this.jTextField2.setBackground(Color.RED);
        blnValido = false;
    } else {
        this.jTextField2.setBackground(Color.WHITE);
    }
        
    if (this.jTextField3.getText().trim().isEmpty()) {
        mensagemErro.append("Quantidade em Estoque não pode ser vazia.\n");
        this.jTextField3.setBackground(Color.RED);
        blnValido = false;
    } else {
        try {
            Integer.parseInt(this.jTextField3.getText());
            this.jTextField3.setBackground(Color.WHITE);
        } catch (NumberFormatException e) {
            mensagemErro.append("Quantidade em Estoque deve ser um número inteiro válido.\n");
            this.jTextField3.setBackground(Color.RED);
            blnValido = false;
        }
    }

    if (this.jTextField4.getText().trim().isEmpty()) {
        mensagemErro.append("Preço Unitário não pode ser vazio.\n");
        this.jTextField4.setBackground(Color.RED);
        blnValido = false;
    } else {
        try {
            new BigDecimal(this.jTextField4.getText().trim());
            this.jTextField4.setBackground(Color.WHITE);
        } catch (NumberFormatException e) {
            mensagemErro.append("Preço Unitário deve ser um número válido.\n");
            this.jTextField4.setBackground(Color.RED);
            blnValido = false;
        }
    }    
    
    if (this.jTextField6.getText().trim().isEmpty()) {
        mensagemErro.append("ID dos Itens não pode ser vazio.\n");
        this.jTextField6.setBackground(Color.RED);
        blnValido = false;
    } else {
        try {
            Integer.parseInt(this.jTextField6.getText());
            this.jTextField6.setBackground(Color.WHITE);
        } catch (NumberFormatException e) {
            mensagemErro.append("ID dos Itens deve ser um número inteiro válido.\n");
            this.jTextField6.setBackground(Color.RED);
            blnValido = false;
        }
    }
    
    if (this.jTextField7.getText().trim().isEmpty()) {
        mensagemErro.append("Código do Fabricante não pode ser vazio.\n");
        this.jTextField7.setBackground(Color.RED);
        blnValido = false;
    } else {
        this.jTextField7.setBackground(Color.WHITE);
    }

    // Se passar na validação, salva o registro
    if (blnValido) {
        pecaAtivo.setIdPecasPK(Integer.valueOf(this.jTextField1.getText()));
        pecaAtivo.setDescricao(this.jTextField2.getText());
        pecaAtivo.setQntdEstoque(Integer.valueOf(this.jTextField3.getText()));
        BigDecimal valor = new BigDecimal(this.jTextField4.getText());
        pecaAtivo.setPrecoUnit(valor);
        pecaAtivo.setCodFabricante(this.jTextField7.getText());            
        pecaAtivo.setIdItensFK(Integer.valueOf(this.jTextField6.getText()));                        
               
        // Verifica se a peça já existe        
        Peca existente = pecaController.buscarPeca(pecaAtivo.getIdPecasPK());
        if (existente != null) {
            pecaAtivo.salvarPeca(pecaAtivo);            
        } else {
            pecaAtivo.adicionarPeca(pecaAtivo);
        }
        
                
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, mensagemErro.toString(), "Erro de Validação", JOptionPane.ERROR_MESSAGE);
    }
           
        
                
    }//GEN-LAST:event_jButton1ActionPerformed

