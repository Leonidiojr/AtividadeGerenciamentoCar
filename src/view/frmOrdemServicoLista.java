    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        // Obtém o próximo ID de ordem de serviço
        int idOrdemServico = ordemServicoController.proximaOrdemServico();

        // Solicitar a placa do veículo
        String placaVeiculo = JOptionPane.showInputDialog(null,"Entre com a placa do Veículo:");

        // Verifica se existe uma OS aberta para a placa informada
        OrdemServico osExistente = ordemServicoController.buscarOrdemServicoAbertaPorPlaca(placaVeiculo);

        if (osExistente != null) {
            int resposta = JOptionPane.showConfirmDialog(null,
                    "Já existe uma ordem de serviço aberta para esta placa: " + osExistente.getPlaca() + "\nVocê deseja iniciar uma nova ordem de serviço?",
                    "Confirmação de Ação",
                    JOptionPane.YES_NO_OPTION);

            if (resposta != JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(null, "Ação cancelada. Não é possível abrir uma nova ordem de serviço enquanto houver uma aberta.");
                return;
            }
        }

// Solicitar dados do cliente e funcionário
        String idCliente = JOptionPane.showInputDialog(null,"Entre com o ID do Cliente:");
        String cpfFuncionario = JOptionPane.showInputDialog(null,"Entre com o CPF do Funcionário:");

// Verificar se os dados são válidos
        Cliente cliente = clienteController.buscarCliente(Integer.valueOf(idCliente));
        Veiculo veiculo = veiculoController.buscarVeiculoPorPlaca(placaVeiculo);
        Funcionario funcionario = funcionarioController.buscarFuncionario(cpfFuncionario);

        if (cliente != null && veiculo != null && funcionario != null) {
            OrdemServico ordemServicoAtivaP = new OrdemServico(
                    idOrdemServico, // ID
                    convertStringToDateTime(getCurrentDateTime()), // Data
                    veiculo.getPlacaPK(), // Placa do veículo
                    BigDecimal.ZERO, // Valor Final
                    BigDecimal.ZERO, // Valor Pago
                    StatusOS.ABERTA, // Status da OS
                    cliente,
                    veiculo,
                    funcionario
            );
            application.MainApp.ordemServicoAtiva = ordemServicoAtivaP;
            ordemServicoController.criarOrdemServico(ordemServicoAtivaP);            

            carregarOrdemServicos();
        } else {
            JOptionPane.showMessageDialog(null, "Cliente, Veículo ou Funcionário não encontrados.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
        }

        
    }//GEN-LAST:event_jButton1ActionPerformed

