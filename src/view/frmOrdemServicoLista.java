    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

    // Solicita o código do ordemServico a ser removido
String idOrdemServico = JOptionPane.showInputDialog("Entre com o código do ordemServico a ser removido:");
if (idOrdemServico != null && !idOrdemServico.isEmpty()) {
            try {
                Integer codOrdemServico = Integer.parseInt(idOrdemServico);

                // Busca o ordemServico pelo código fornecido
                OrdemServico ordemServico = ordemServicoController.buscarOrdemServico(codOrdemServico);

                if (ordemServico != null) {
                    int resposta = JOptionPane.showConfirmDialog(null,
                            "OrdemServico a ser removido: " + ordemServico.getPlaca() + "\nVocê tem certeza que deseja remover este ordemServico?",
                            "Confirmação de Remoção",
                            JOptionPane.YES_NO_OPTION);

                    if (resposta == JOptionPane.YES_OPTION) {
                        ordemServicoController.deletarOrdemServico(codOrdemServico);
                        JOptionPane.showMessageDialog(null, "OrdemServico removido com sucesso.");
                        ordemServicoController.listarOrdemServico();
                        carregarOrdemServicos();
                    } else {
                        JOptionPane.showMessageDialog(null, "A remoção do ordemServico não foi confirmada.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "OrdemServico não encontrado ou nome completo não disponível.");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Código do ordemServico inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Código do ordemServico não pode ser vazio.", "Erro", JOptionPane.ERROR_MESSAGE);
        }



    }//GEN-LAST:event_jButton3ActionPerformed

