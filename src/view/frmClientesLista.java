    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
    // Solicita o código do cliente a ser adicionado
    String idCliente = DialogBoxes.InputBox("Entre com o CPF do cliente a ser adicionado:", "Adicionar cliente");

    // Busca o cliente pelo código fornecido
    Cliente cliente = clienteController.buscarClienteCPF(idCliente);

    if (cliente != null && cliente.getCPF() != null) {
        int resposta = JOptionPane.showConfirmDialog(null,
                "Cliente já existe no sistema: " + cliente.getCPF() + "\nVocê deseja abrir as informações deste cliente?",
                "Confirmação de Remoção",
                JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {
            application.MainApp.clienteAtivo = cliente;
            frmClientesEdita cadastroClientes = new frmClientesEdita(this, true);
            cadastroClientes.setLocationRelativeTo(this);
            cadastroClientes.setVisible(true);
            clienteController.listarClientes();
        } else {
            JOptionPane.showMessageDialog(null, "A visualização do cliente não foi confirmada.");
        }
    } else {
        int resposta = JOptionPane.showConfirmDialog(null,
                "Cliente não existe no sistema: " + idCliente + "\nVocê deseja adicionar este cliente?",
                "Confirmação de Adição",
                JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {
            Cliente clienteAtivo = new Cliente(0, "", "", "", "", "", "", "", "", "", "", 1, "", null, " " , "", "", "", "", "", "");               
            clienteAtivo.setCPF(idCliente);
            application.MainApp.clienteAtivo = clienteAtivo;
            frmClientesEdita cadastroClientes = new frmClientesEdita(this, true);
            cadastroClientes.setLocationRelativeTo(this);
            cadastroClientes.setVisible(true);
            
            if (!clienteAtivo.getCPF().isEmpty()) {
                clienteController.criarCliente(clienteAtivo);
            }
            carregarClientes();
        } else {
            JOptionPane.showMessageDialog(null, "A adição do cliente não foi confirmada.");
        }
    }

        
    }//GEN-LAST:event_jButton1ActionPerformed

