    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

         // Solicita o código do cliente a ser removido
        String idCliente = DialogBoxes.InputBox("Entre com o codigo do cliente a ser removido:", "Remover cliente");
        Integer codCliente = Integer.parseInt(idCliente);
        
        // Busca o cliente pelo código fornecido
        Cliente cliente = clienteController.buscarCliente(codCliente);

        if (cliente != null && cliente.getCPF()!= null) {
            int resposta = JOptionPane.showConfirmDialog(null,
                    "Cliente a ser removido: " + cliente.getNomeCompleto() + "\nVocê tem certeza que deseja remover este cliente?",
                    "Confirmação de Remoção",
                    JOptionPane.YES_NO_OPTION);

            if (resposta == JOptionPane.YES_OPTION) {
                clienteController.deletarCliente(codCliente);
                JOptionPane.showMessageDialog(null, "Cliente removido com sucesso.");
                clienteController.listarClientes();
                carregarClientes();
            } else {
                JOptionPane.showMessageDialog(null, "A remoção do cliente não foi confirmada.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Cliente não encontrado ou nome completo não disponível.");
        } 



    }//GEN-LAST:event_jButton3ActionPerformed

