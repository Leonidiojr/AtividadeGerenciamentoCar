    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        int selectedRow = jTable1.getSelectedRow(); // A linha selecionada

        if (selectedRow != -1) { // Verifica se uma linha foi selecionada
            try {
                // Pega o valor da célula da primeira coluna
                Object value = jTable1.getValueAt(selectedRow, 0);
                Integer idClienteAtivo = null;

                // Verifica o tipo do valor da célula e realiza a conversão adequada
                if (value instanceof String) {
                    idClienteAtivo = Integer.valueOf((String) value);
                } else if (value instanceof Integer) {
                    idClienteAtivo = (Integer) value;
                }

                if (idClienteAtivo != null) {
                    // Realiza a busca pelo cliente ativo
                    clienteAtivo = clienteController.buscarCliente(idClienteAtivo);

                    // Abre a janela de edição de clientes
                    frmClientesEdita cadastroClients = new frmClientesEdita(this, true);
                    cadastroClients.setLocationRelativeTo(this);
                    cadastroClients.setVisible(true);

                    // Exibe o valor do ID do cliente
                    System.out.println("ID Cliente Ativo: " + idClienteAtivo);
                    
                } else {
                    System.out.println("Valor do ID do cliente é nulo.");
                }
            } catch (NumberFormatException e) {
                // Trata a exceção caso o valor não seja um número válido
                System.out.println("Formato de número inválido: " + e.getMessage());
                e.printStackTrace();
            } catch (ClassCastException e) {
                // Trata a exceção caso o valor não seja do tipo esperado
                System.out.println("Valor do ID do cliente inválido: " + e.getMessage());
                e.printStackTrace();
            } catch (Exception e) {
                // Trata possíveis exceções ao buscar cliente e abrir a janela
                System.out.println("Erro ao buscar cliente ou abrir a janela: " + e.getMessage());
                e.printStackTrace();
            }
        } else {
            // Caso não haja nenhuma linha selecionada
            System.out.println("Nenhuma linha selecionada.");
        }
        carregarClientes();
        
    }//GEN-LAST:event_jTable1MouseClicked

